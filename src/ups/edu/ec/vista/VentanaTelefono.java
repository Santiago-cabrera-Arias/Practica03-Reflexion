    private void eventoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eventoMouseClicked

        int fila = tblDatos.getSelectedRow();

        int id = (Integer) tblDatos.getValueAt(fila, 0);
        String numero = (String) tblDatos.getValueAt(fila, 1);
        String operadora = (String) tblDatos.getValueAt(fila, 2);
        String tipo = (String) tblDatos.getValueAt(fila, 3);

        txtId.setText(id + "");
        txtNumero.setText(numero);
        txtOperadora.setText(operadora);
        txtTipo.setText(tipo);


    }//GEN-LAST:event_eventoMouseClicked

    private void btnLimpiarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarTablaActionPerformed

        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);

        for (int i = 0; i < tblDatos.getRowCount(); i++) {

            modelo.removeRow(i);

        }

        tblDatos.setModel(modelo);

    }//GEN-LAST:event_btnLimpiarTablaActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        // int id = (Integer.parseInt(txtId.getText()));
        String numero = txtNumero.getText();
        String operadora = txtOperadora.getText();
        String tipo = txtTipo.getText();

        if (numero.isEmpty() || operadora.isEmpty() || tipo.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campo");

        } else {

            Telefono t = new Telefono(controladorTelefono.generarId(), numero, operadora, tipo);

            controladorTelefono.create(t);

            JOptionPane.showMessageDialog(this, "Telefono registrado.");

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

