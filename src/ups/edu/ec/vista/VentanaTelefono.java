    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar.");
        int id = Integer.parseInt(txtId.getText());

        Telefono t = new Telefono(id);

        boolean cent = controladorTelefono.delete(t);

        if (cent && opcion == JOptionPane.YES_OPTION) {

            datosDeLaTabla();

            JOptionPane.showMessageDialog(this, "Dato eliminado.");

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

