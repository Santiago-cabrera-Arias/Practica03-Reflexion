    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int eliminacion = JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar");
        String cedula = txtCedula.getText();

        Persona p = new Persona(cedula);

        boolean cent = controladorPersona.delete(p);

        if (cent && eliminacion == JOptionPane.YES_OPTION) {

            datosDelaTabla();

            JOptionPane.showMessageDialog(this, "datos eliminado con exito");

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnLimpiarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarTablaActionPerformed

        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);

        for (int i = 0; i < tblDatos.getRowCount(); i++) {

            modelo.removeRow(i);

        }

        tblDatos.setModel(modelo);

    }//GEN-LAST:event_btnLimpiarTablaActionPerformed


