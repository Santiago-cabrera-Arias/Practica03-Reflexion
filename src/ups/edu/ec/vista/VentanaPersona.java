    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        int id = Integer.parseInt(txtId.getText());
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();

        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty()
                || direccion.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene todos los campos");

        } else {

            Persona persona = new Persona(controladorPersona.generarId(), cedula, nombre, apellido, direccion);

            controladorPersona.create(persona);

            JOptionPane.showMessageDialog(this, "Se ha registrado correctamente");

        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String cedula = txtBuscarPorCedula.getText();

        if (cedula.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene el campo ");

        } else {

            Persona p = new Persona(cedula);

            listarDatos(controladorPersona.read(p));

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        int id = Integer.parseInt(txtId.getText());
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();

        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene todos los campos");

        } else {

            Persona p = new Persona(id, cedula, nombre, apellido, direccion);

            boolean cent = controladorPersona.update(p);

            if (cent) {

                listarDatos(p);

            }

        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void MouseClickedEventos(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MouseClickedEventos

        int fila = tblDatos.getSelectedRow();

        int id = (Integer) tblDatos.getValueAt(fila, 0);
        String cedula = (String) tblDatos.getValueAt(fila, 1);
        String nombre = (String) tblDatos.getValueAt(fila, 2);
        String apellido = (String) tblDatos.getValueAt(fila, 3);
        String direccion = (String) tblDatos.getValueAt(fila, 4);

        txtId.setText(id + "");
        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(apellido);
        txtDireccion.setText(direccion);


    }//GEN-LAST:event_MouseClickedEventos

