    public void listarDatos(Persona persona) {

        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);

        Object[] objeto = {persona.getId(), persona.getCedula(), persona.getNombre(), persona.getApellido(),
            persona.getDireccion()};

        modelo.addRow(objeto);

        tblDatos.setModel(modelo);

    }

    public void datosDelaTabla() {

        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);

        List<Persona> listaDePersona = controladorPersona.findAll();

         controladorPersona.imprimirCualquierLista(listaDePersona);
        
        TableRowSorter<TableModel> sorte = new TableRowSorter<>(modelo);
        tblDatos.setRowSorter(sorte);

        List<RowSorter.SortKey> sortKeys = new ArrayList<>();
        sortKeys.add(new RowSorter.SortKey(3, SortOrder.ASCENDING));
        sorte.setSortKeys(sortKeys);

        for (Persona persona : listaDePersona) {

            Object[] objeto = {persona.getId(), persona.getCedula(), persona.getNombre(), persona.getApellido(),
                persona.getDireccion()};

            modelo.addRow(objeto);

            tblDatos.setModel(modelo);
        }

    }

